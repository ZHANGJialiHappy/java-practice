public class Student {
    private String name;
    private int age;

    public Student(String name) {
        this.name = name;
        age = 0;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getName() {
        return name;
    }

}
